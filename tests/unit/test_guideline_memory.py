def test_orchestrator_routes_explicit_memory_requests_to_guideline_agent(tmp_path: Path) -> None:
    service = _service(tmp_path / "guidelines.json")
    fake = FakeGenAIClient()
    orchestrator = MainOrchestratorAgent(
        AgentReasoner(fake),  # type: ignore[arg-type]
        guideline_service=service,
    )

    response = orchestrator.handle(
        AgentRequest(
            user_message="remember that I prefer semiconductors",
            chat_id="chat",
        )
    )
    nodes = service.list_nodes(active_only=False)

    assert response.selected_agent == "guideline_memory_agent"
    assert "Created guideline node" in response.final_answer
    assert len(nodes) == 1
    assert nodes[0].category == "investment_preference"


def test_build_runtime_wires_guideline_memory_service(monkeypatch, tmp_path: Path) -> None:
    path = tmp_path / "runtime-guidelines.json"
    monkeypatch.setenv("GUIDELINE_MEMORY_PATH", str(path))

    runtime = build_runtime()

    assert runtime.settings.guideline_memory_path == str(path)
    assert runtime.guideline_document_store.path == path
    assert runtime.guideline_memory_service.render_markdown() == ""
