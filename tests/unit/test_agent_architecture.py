class FakeGenAIClient:
    def __init__(self) -> None:
        self.calls: list[dict[str, object]] = []
        self.model_purposes: list[str] = []

    def chat_model_for(self, purpose: str | None = None) -> str:
        resolved = purpose or "default"
        self.model_purposes.append(resolved)
        return f"{resolved}-model"

    def generate_structured(
        self,
        schema: type,
        system_prompt: str,
        chat_message: object,
        *,
        model: str | None = None,
        temperature: float = 0.2,
        max_tokens: int | None = None,
    ) -> object:
        self.calls.append(
            {
                "schema": schema.__name__,
                "system_prompt": system_prompt,
                "chat_message": chat_message,
                "model": model,
                "temperature": temperature,
                "max_tokens": max_tokens,
            }
        )
        if schema is AgentCritique:
            return AgentCritique(
                passed=True,
                summary="Looks complete.",
                confidence=0.9,
            )
        return AgentPlan(
            intent=AgentIntent(kind=IntentKind.UNKNOWN),
            summary="Plan generated by fake GenAI.",
            required_context=["context"],
            assumptions=["assumption"],
            risks=["risk"],
        )


def _reasoner() -> AgentReasoner:
    return AgentReasoner(FakeGenAIClient())  # type: ignore[arg-type]


