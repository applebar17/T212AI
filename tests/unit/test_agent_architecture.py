def test_chat_history_retains_and_selects_recent_messages() -> None:
    policy = ChatHistoryPolicy(max_messages_per_chat=3, context_messages=2)
    manager = ChatHistoryManager(
        store=InMemoryChatHistoryStore(max_messages_per_chat=policy.max_messages_per_chat),
        policy=policy,
    )

    manager.record_user_message("chat", "one")
    manager.record_assistant_message("chat", "two")
    manager.record_user_message("chat", "three")
    manager.record_assistant_message("chat", "four")
    window = manager.get_context_window("chat")

    assert window.retained_count == 3
    assert window.selected_count == 2
    assert [message.content for message in window.messages] == ["three", "four"]


def test_reasoner_selects_smart_model_for_critical_tasks() -> None:
    fake = FakeGenAIClient()
    reasoner = AgentReasoner(fake)  # type: ignore[arg-type]

    plan = reasoner.build_plan(
        agent_name="order_agent",
        purpose="orders",
        guidelines="be safe",
        toolbox_summary="orders",
        task_complexity=TaskComplexity.CRITICAL,
        user_request="cancel my order",
        chat_history=ChatHistoryWindow(chat_id="chat"),
        intent=AgentIntent(kind=IntentKind.CANCEL_ORDER),
    )

    assert plan.task_complexity == TaskComplexity.CRITICAL
    assert fake.model_purposes == ["smart"]
    assert fake.calls[0]["model"] == "smart-model"


