class FakeGenAIClient:
    def __init__(self) -> None:
        self.calls: list[dict[str, object]] = []
        self.model_purposes: list[str] = []

    def chat_model_for(self, purpose: str | None = None) -> str:
        resolved = purpose or "default"
        self.model_purposes.append(resolved)
        return f"{resolved}-model"

    def generate_structured(
        self,
        schema: type,
        system_prompt: str,
        chat_message: object,
        *,
        model: str | None = None,
        temperature: float = 0.2,
        max_tokens: int | None = None,
    ) -> object:
        self.calls.append(
            {
                "schema": schema.__name__,
                "system_prompt": system_prompt,
                "chat_message": chat_message,
                "model": model,
                "temperature": temperature,
                "max_tokens": max_tokens,
            }
        )
        if schema is AgentCritique:
            return AgentCritique(
                passed=True,
                summary="Looks complete.",
                confidence=0.9,
            )
        return AgentPlan(
            intent=AgentIntent(kind=IntentKind.UNKNOWN),
            summary="Plan generated by fake GenAI.",
            required_context=["context"],
            assumptions=["assumption"],
            risks=["risk"],
        )


def _reasoner() -> AgentReasoner:
    return AgentReasoner(FakeGenAIClient())  # type: ignore[arg-type]


def test_chat_history_retains_and_selects_recent_messages() -> None:
    policy = ChatHistoryPolicy(max_messages_per_chat=3, context_messages=2)
    manager = ChatHistoryManager(
        store=InMemoryChatHistoryStore(max_messages_per_chat=policy.max_messages_per_chat),
        policy=policy,
    )

    manager.record_user_message("chat", "one")
    manager.record_assistant_message("chat", "two")
    manager.record_user_message("chat", "three")
    manager.record_assistant_message("chat", "four")
    window = manager.get_context_window("chat")

    assert window.retained_count == 3
    assert window.selected_count == 2
    assert [message.content for message in window.messages] == ["three", "four"]


def test_reasoner_selects_smart_model_for_critical_tasks() -> None:
    fake = FakeGenAIClient()
    reasoner = AgentReasoner(fake)  # type: ignore[arg-type]

    plan = reasoner.build_plan(
        agent_name="order_agent",
        purpose="orders",
        guidelines="be safe",
        toolbox_summary="orders",
        task_complexity=TaskComplexity.CRITICAL,
        user_request="cancel my order",
        chat_history=ChatHistoryWindow(chat_id="chat"),
        intent=AgentIntent(kind=IntentKind.CANCEL_ORDER),
    )

    assert plan.task_complexity == TaskComplexity.CRITICAL
    assert fake.model_purposes == ["smart"]
    assert fake.calls[0]["model"] == "smart-model"


def test_specialist_agent_returns_standardized_response() -> None:
    agent = PortfolioAnalystAgent(_reasoner())

    response = agent.handle(
        AgentRequest(user_message="summarize my portfolio", chat_id="chat"),
        intent=AgentIntent(kind=IntentKind.PORTFOLIO_SUMMARY),
    )

    assert response.selected_agent == "portfolio_analyst"
    assert response.plan is not None
    assert "portfolio_analyst prepared a plan" in response.final_answer


def test_orchestrator_routes_representative_requests() -> None:
    orchestrator = MainOrchestratorAgent(_reasoner())

    cases = {
        "show my portfolio": "portfolio_analyst",
        "cancel my oldest order": "order_agent",
        "what happened in the market today": "market_analyst",
        "analyze Apple earnings": "company_analyst",
        "hello": "main_orchestrator",
        "/help": "main_orchestrator",
    }
    for message, expected_agent in cases.items():
        response = orchestrator.handle(AgentRequest(user_message=message, chat_id="chat"))
        assert response.selected_agent == expected_agent


def test_agent_judge_uses_standardized_critique() -> None:
    judge = AgentJudge(_reasoner())
    response = PortfolioAnalystAgent(_reasoner()).handle(
        AgentRequest(user_message="portfolio", chat_id="chat"),
        intent=AgentIntent(kind=IntentKind.PORTFOLIO_SUMMARY),
    )

    critique = judge.review(
        request=AgentRequest(user_message="portfolio", chat_id="chat"),
        response=response,
    )

    assert critique.passed
    assert critique.summary == "Looks complete."


def test_telegram_agent_handler_records_user_and_assistant_history() -> None:
    history = ChatHistoryManager(
        policy=ChatHistoryPolicy(max_messages_per_chat=30, context_messages=16)
    )
    main_agent = MainOrchestratorAgent(_reasoner())
    handler = build_default_message_handler(
        main_agent=main_agent,
        history_manager=history,
    )

    response = handler(
        TelegramInboundMessage(
            chat_id=123,
            text="show my portfolio",
            message_id=1,
        )
    )
    window = history.get_context_window(123)

    assert isinstance(response, TelegramOutboundMessage)
    assert "portfolio_analyst prepared a plan" in response.text
    assert window.retained_count == 2
    assert [message.role for message in window.messages] == ["user", "assistant"]


def test_telegram_agent_handler_falls_back_when_genai_is_not_configured(monkeypatch) -> None:
    monkeypatch.delenv("OPENAI_API_KEY", raising=False)
    monkeypatch.setenv("AZURE_OPENAI_ENABLED", "false")

    handler = build_agent_message_handler_if_configured()
    response = handler(
        TelegramInboundMessage(
            chat_id=123,
            text="show my portfolio",
            message_id=1,
        )
    )

    assert isinstance(response, TelegramOutboundMessage)
    assert "agent runtime is not wired yet" in response.text
